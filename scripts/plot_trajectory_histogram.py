"""
Time-resolved distance histogram — composability gap analysis.

Reads trajectory_distances.json (generated by measure_composability_gap.py)
and plots overlapping histograms of per-element MSE from SuperDiff-AND,
one histogram per timestep bucket.  Opacity encodes time:

    transparent  →  early denoising steps  (high noise, near x_T)
    opaque       →  late denoising steps   (clean image, near x_0)

Three side-by-side panels show each generation condition:
    Monolithic  |  Solo c₁  |  Solo c₂

Two rendering modes (select with --curve):
    bars   (default)  — go.Histogram, overlapping bars, barmode="overlay"
    curve  (--curve)  — scipy gaussian_kde + go.Scatter filled curves;
                        reads more cleanly with many overlapping layers

Usage
-----
conda run -n superdiff python scripts/plot_trajectory_histogram.py \\
    [--data-dir experiments/inversion/gap_analysis] \\
    [--output-dir experiments/inversion/gap_analysis] \\
    [--n-buckets 8] \\
    [--curve]
"""

import argparse
import json
import sys
from pathlib import Path

import numpy as np

try:
    import pandas as pd
except ImportError:
    sys.exit("pandas required:  pip install pandas")

try:
    import plotly.graph_objects as go
    from plotly.subplots import make_subplots
except ImportError:
    sys.exit("plotly required:  pip install plotly")

try:
    from scipy.stats import gaussian_kde
except ImportError:
    gaussian_kde = None   # only required for --curve mode


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

COND_KEYS  = ["d_t_mono", "d_t_c1", "d_t_c2"]
COND_LABEL = {
    "d_t_mono": "Monolithic  <i>SD3.5(c₁ and c₂)</i>",
    "d_t_c1":   "Solo c₁  <i>SD3.5(c₁)</i>",
    "d_t_c2":   "Solo c₂  <i>SD3.5(c₂)</i>",
}

# Condition colours as (R, G, B) — Tableau 10 subset
COND_RGB = {
    "d_t_mono": (225, 87,  89),
    "d_t_c1":   (78,  121, 167),
    "d_t_c2":   (89,  161, 79),
}

# Opacity ramp: step 0 (pure noise) → fully transparent
#               step T (clean image) → nearly opaque
ALPHA_EARLY = 0.08
ALPHA_LATE  = 0.88


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------

def load_records(data_dir: Path) -> list:
    path = data_dir / "trajectory_distances.json"
    if not path.exists():
        sys.exit(
            f"File not found: {path}\n"
            "Re-run measure_composability_gap.py to generate it."
        )
    with open(path) as f:
        return json.load(f)


# ---------------------------------------------------------------------------
# Figure
# ---------------------------------------------------------------------------

def build_figure(records: list, n_buckets: int) -> go.Figure:
    """
    Three panels (one per condition), N_BUCKETS overlapping histograms each.

    Step 0 is dropped: all distances are ~0 there (shared initial noise),
    producing an uninformative spike that compresses the x-axis.
    """
    df = pd.DataFrame(records)

    # Drop step 0 — distances are trivially 0 at the start for all conditions
    df = df[df["step"] > 0].copy()

    max_step = int(df["step"].max())
    edges    = np.linspace(1, max_step + 1, n_buckets + 1, dtype=int)
    edges[-1] = max_step + 1   # ensure the last step is included

    bucket_ranges = [(int(edges[i]), int(edges[i + 1]) - 1) for i in range(n_buckets)]
    bucket_labels = [f"Steps {lo}–{hi}" for lo, hi in bucket_ranges]

    # Assign each row to a bucket
    df["bucket"] = pd.cut(
        df["step"],
        bins=edges,
        labels=False,
        right=False,
        include_lowest=True,
    ).astype(int)

    # Opacity: linearly from ALPHA_EARLY (bucket 0) to ALPHA_LATE (last bucket)
    alphas = np.linspace(ALPHA_EARLY, ALPHA_LATE, n_buckets)

    # Fixed bin layout shared across all traces for direct visual comparison
    x_max    = max(df[k].quantile(0.995) for k in COND_KEYS) * 1.08
    bin_size = x_max / 45

    # ---------------------------------------------------------------------------
    fig = make_subplots(
        rows=1,
        cols=3,
        subplot_titles=[COND_LABEL[k] for k in COND_KEYS],
        shared_yaxes=True,
        shared_xaxes=True,
        horizontal_spacing=0.03,
    )

    for col_idx, cond in enumerate(COND_KEYS, start=1):
        r, g, b = COND_RGB[cond]

        for bucket in range(n_buckets):
            sub   = df[df["bucket"] == bucket]
            alpha = float(alphas[bucket])
            lo, hi = bucket_ranges[bucket]

            # Only show legend entries once (first panel)
            fig.add_trace(
                go.Histogram(
                    x=sub[cond].values,
                    name=bucket_labels[bucket],
                    legendgroup=f"b{bucket}",
                    showlegend=(col_idx == 1),
                    xbins=dict(start=0.0, end=float(x_max), size=float(bin_size)),
                    histnorm="probability density",
                    marker=dict(
                        color=f"rgba({r},{g},{b},{alpha:.3f})",
                        line=dict(width=0),
                    ),
                    hovertemplate=(
                        f"Steps {lo}–{hi}<br>"
                        "MSE: %{x:.5f}<br>"
                        "Density: %{y:.2f}"
                        "<extra></extra>"
                    ),
                ),
                row=1,
                col=col_idx,
            )

    # ---------------------------------------------------------------------------
    # Layout
    fig.update_layout(
        title=dict(
            text=(
                "Temporal Evolution of Distance from SuperDiff-AND<br>"
                "<sup>Per-element MSE pooled across all seeds and pairs — "
                "opacity encodes denoising progress "
                "(transparent = noisy, opaque = clean)</sup>"
            ),
            font=dict(size=16),
            x=0.5,
            xanchor="center",
        ),
        barmode="overlay",
        template="plotly_white",
        font=dict(family="Arial, sans-serif", size=12),
        legend=dict(
            title=dict(text="Denoising step bucket", font=dict(size=11)),
            borderwidth=1,
            bordercolor="#CCCCCC",
            tracegroupgap=2,
        ),
        margin=dict(l=75, r=30, t=120, b=70),
        width=1150,
        height=490,
        plot_bgcolor="white",
        paper_bgcolor="white",
    )

    # Axis labels — x shared, y only on first panel
    for col_idx in range(1, 4):
        fig.update_xaxes(
            title_text="Per-element MSE  (z<sub>t</sub><sup>cond</sup> − z<sub>t</sub><sup>AND</sup>)",
            range=[0, x_max],
            tickformat=".3f",
            gridcolor="#E8E8E8",
            zeroline=True,
            zerolinecolor="#CCCCCC",
            row=1,
            col=col_idx,
        )

    fig.update_yaxes(
        title_text="Probability density",
        gridcolor="#E8E8E8",
        zeroline=False,
        row=1,
        col=1,
    )

    # Add a thin vertical line at the pooled mean of the terminal step (step = max_step)
    for col_idx, cond in enumerate(COND_KEYS, start=1):
        terminal_mean = float(df[df["step"] == max_step][cond].mean())
        fig.add_vline(
            x=terminal_mean,
            line=dict(color=f"rgb({COND_RGB[cond][0]},{COND_RGB[cond][1]},{COND_RGB[cond][2]})",
                      width=1.5, dash="dot"),
            row=1,
            col=col_idx,
        )

    # Annotation: dataset summary
    n_pairs = df["pair"].nunique()
    n_seeds = df["seed"].nunique()
    n_steps = max_step
    fig.add_annotation(
        text=f"N = {n_pairs} pairs × {n_seeds} seeds × {n_steps} steps "
             f"= {n_pairs * n_seeds * n_steps:,} samples total",
        xref="paper", yref="paper",
        x=0.5, y=-0.13,
        showarrow=False,
        font=dict(size=10, color="#666666"),
        xanchor="center",
    )

    return fig


