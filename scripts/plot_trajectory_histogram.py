def build_figure_curve(records: list, n_buckets: int) -> go.Figure:
    """
    Same bucketing and opacity logic as build_figure(), but renders each
    bucket as a smooth KDE curve (go.Scatter fill="tozeroy") instead of
    bars.  With 8+ overlapping layers, filled curves read more cleanly than
    bars because partial occlusion is less aggressive.

    Fill opacity  = alphas[bucket]          (same ramp as bar variant)
    Line opacity  = min(alphas[bucket] + 0.25, 1.0)   (slightly bolder edge)
    """
    if gaussian_kde is None:
        sys.exit("scipy is required for --curve:  pip install scipy")

    df = pd.DataFrame(records)
    df = df[df["step"] > 0].copy()

    max_step = int(df["step"].max())
    edges    = np.linspace(1, max_step + 1, n_buckets + 1, dtype=int)
    edges[-1] = max_step + 1

    bucket_ranges = [(int(edges[i]), int(edges[i + 1]) - 1) for i in range(n_buckets)]
    bucket_labels = [f"Steps {lo}–{hi}" for lo, hi in bucket_ranges]

    df["bucket"] = pd.cut(
        df["step"], bins=edges, labels=False, right=False, include_lowest=True,
    ).astype(int)

    alphas  = np.linspace(ALPHA_EARLY, ALPHA_LATE, n_buckets)
    x_max   = max(df[k].quantile(0.995) for k in COND_KEYS) * 1.08
    x_grid  = np.linspace(0.0, x_max, 400)

    fig = make_subplots(
        rows=1, cols=3,
        subplot_titles=[COND_LABEL[k] for k in COND_KEYS],
        shared_yaxes=True,
        shared_xaxes=True,
        horizontal_spacing=0.03,
    )

    for col_idx, cond in enumerate(COND_KEYS, start=1):
        r, g, b = COND_RGB[cond]

        for bucket in range(n_buckets):
            sub    = df[df["bucket"] == bucket][cond].dropna().values
            if len(sub) < 4:
                continue

            alpha_fill = float(alphas[bucket])
            alpha_line = min(alpha_fill + 0.25, 1.0)
            lo, hi     = bucket_ranges[bucket]

            kde    = gaussian_kde(sub, bw_method="scott")
            y_kde  = kde(x_grid)

            fig.add_trace(
                go.Scatter(
                    x=np.concatenate([[x_grid[0]], x_grid, [x_grid[-1]]]),
                    y=np.concatenate([[0.0],       y_kde,  [0.0]]),
                    fill="tozeroy",
                    fillcolor=f"rgba({r},{g},{b},{alpha_fill:.3f})",
                    line=dict(
                        color=f"rgba({r},{g},{b},{alpha_line:.3f})",
                        width=1.5,
                    ),
                    name=bucket_labels[bucket],
                    legendgroup=f"b{bucket}",
                    showlegend=(col_idx == 1),
                    hovertemplate=(
                        f"Steps {lo}–{hi}<br>"
                        "MSE: %{x:.5f}<br>"
                        "Density: %{y:.2f}"
                        "<extra></extra>"
                    ),
                    mode="lines",
                ),
                row=1, col=col_idx,
            )

    # Shared layout (identical to bar variant except no barmode)
    fig.update_layout(
        title=dict(
            text=(
                "Temporal Evolution of Distance from SuperDiff-AND<br>"
                "<sup>Per-element MSE pooled across all seeds and pairs — "
                "opacity encodes denoising progress "
                "(transparent = noisy, opaque = clean)</sup>"
            ),
            font=dict(size=16),
            x=0.5,
            xanchor="center",
        ),
        template="plotly_white",
        font=dict(family="Arial, sans-serif", size=12),
        legend=dict(
            title=dict(text="Denoising step bucket", font=dict(size=11)),
            borderwidth=1,
            bordercolor="#CCCCCC",
            tracegroupgap=2,
        ),
        margin=dict(l=75, r=30, t=120, b=70),
        width=1150,
        height=490,
        plot_bgcolor="white",
        paper_bgcolor="white",
    )

    for col_idx in range(1, 4):
        fig.update_xaxes(
            title_text="Per-element MSE  (z<sub>t</sub><sup>cond</sup> − z<sub>t</sub><sup>AND</sup>)",
            range=[0, x_max],
            tickformat=".3f",
            gridcolor="#E8E8E8",
            zeroline=True,
            zerolinecolor="#CCCCCC",
            row=1, col=col_idx,
        )

    fig.update_yaxes(
        title_text="Probability density",
        gridcolor="#E8E8E8",
        zeroline=False,
        row=1, col=1,
    )

    for col_idx, cond in enumerate(COND_KEYS, start=1):
        terminal_mean = float(df[df["step"] == max_step][cond].mean())
        fig.add_vline(
            x=terminal_mean,
            line=dict(
                color=f"rgb({COND_RGB[cond][0]},{COND_RGB[cond][1]},{COND_RGB[cond][2]})",
                width=1.5, dash="dot",
            ),
            row=1, col=col_idx,
        )

    n_pairs = df["pair"].nunique()
    n_seeds = df["seed"].nunique()
    fig.add_annotation(
        text=f"N = {n_pairs} pairs × {n_seeds} seeds × {max_step} steps "
             f"= {n_pairs * n_seeds * max_step:,} samples total  |  KDE bandwidth: Scott's rule",
        xref="paper", yref="paper",
        x=0.5, y=-0.13,
        showarrow=False,
        font=dict(size=10, color="#666666"),
        xanchor="center",
    )

    return fig


# ---------------------------------------------------------------------------
# Divergence metrics (Monte Carlo KDE)
# ---------------------------------------------------------------------------

# Pairings to compare: (key_p, key_q, label)
COND_PAIRS_DIV = [
    ("d_t_mono", "d_t_c1", "Mono vs Solo c₁"),
    ("d_t_mono", "d_t_c2", "Mono vs Solo c₂"),
    ("d_t_c1",   "d_t_c2", "Solo c₁ vs Solo c₂"),
]

PAIR_COLORS_DIV = {
    "Mono vs Solo c₁":    "#E15759",
    "Mono vs Solo c₂":    "#4E79A7",
    "Solo c₁ vs Solo c₂": "#59A14F",
}


def _mc_kl(kde_p, kde_q, n_samples: int, seed: int) -> float:
    """Monte Carlo estimate of KL(P || Q) = E_P[log P(x) − log Q(x)]."""
    samples = kde_p.resample(n_samples, seed=seed).flatten()
    return float(np.mean(kde_p.logpdf(samples) - kde_q.logpdf(samples)))


def compute_divergences(df: "pd.DataFrame", n_buckets: int, n_samples: int = 20_000) -> "pd.DataFrame":
    """
    For each timestep bucket fit a KDE per condition, then compute pairwise
    KL(P||Q), KL(Q||P), and Jeffrey's divergence J = (KL(P||Q)+KL(Q||P))/2.

    Returns a DataFrame with columns:
        bucket, label, lo, hi, kl_pq, kl_qp, jeffreys
    """
    if gaussian_kde is None:
        sys.exit("scipy is required for --divergence:  pip install scipy")

    max_step = int(df["step"].max())
    edges    = np.linspace(1, max_step + 1, n_buckets + 1, dtype=int)
    edges[-1] = max_step + 1

    bucket_ranges = [(int(edges[i]), int(edges[i + 1]) - 1) for i in range(n_buckets)]

    df = df.copy()
    df["bucket"] = pd.cut(
        df["step"], bins=edges, labels=False, right=False, include_lowest=True,
    ).astype(int)

    rows = []
    for bucket in range(n_buckets):
        sub = df[df["bucket"] == bucket]
        lo, hi = bucket_ranges[bucket]

        # Fit one KDE per condition key
        kdes = {}
        for key in COND_KEYS:
            vals = sub[key].dropna().values
            if len(vals) < 8:
                kdes[key] = None
            else:
                kdes[key] = gaussian_kde(vals, bw_method="scott")

        for key_p, key_q, label in COND_PAIRS_DIV:
            if kdes[key_p] is None or kdes[key_q] is None:
                continue
            kl_pq = _mc_kl(kdes[key_p], kdes[key_q], n_samples, seed=bucket)
            kl_qp = _mc_kl(kdes[key_q], kdes[key_p], n_samples, seed=bucket + 100)
            rows.append({
                "bucket":   bucket,
                "label":    label,
                "lo":       lo,
                "hi":       hi,
                "kl_pq":    kl_pq,
                "kl_qp":    kl_qp,
                "jeffreys": (kl_pq + kl_qp) / 2.0,
            })

    return pd.DataFrame(rows)


def build_divergence_figure(records: list, n_buckets: int, n_samples: int = 20_000) -> go.Figure:
    """
    Two side-by-side grouped bar charts:
      Left  — Jeffrey's divergence  J(P,Q) = (KL(P||Q) + KL(Q||P)) / 2
      Right — Asymmetric KL divergences KL(P||Q) and KL(Q||P)

    X-axis: timestep bucket (early → late).
    Each bar group contains one bar per condition pair.
    """
    df = pd.DataFrame(records)
    df = df[df["step"] > 0].copy()

    div_df = compute_divergences(df, n_buckets=n_buckets, n_samples=n_samples)

    bucket_labels = [
        f"Steps {r['lo']}–{r['hi']}"
        for _, r in div_df.drop_duplicates("bucket").sort_values("bucket").iterrows()
    ]

    fig = make_subplots(
        rows=1, cols=2,
        subplot_titles=[
            "Jeffrey's Divergence  <i>J(P, Q) = ½(KL(P‖Q) + KL(Q‖P))</i>",
            "Asymmetric KL Divergences",
        ],
        horizontal_spacing=0.10,
    )

    labels_seen: set = set()

    for _, row in div_df.iterrows():
        label = row["label"]
        color = PAIR_COLORS_DIV[label]
        x_lbl = f"Steps {int(row['lo'])}–{int(row['hi'])}"
        show  = label not in labels_seen
        labels_seen.add(label)

        # --- Left panel: Jeffrey's ---
        fig.add_trace(
            go.Bar(
                x=[x_lbl],
                y=[row["jeffreys"]],
                name=label,
                legendgroup=label,
                showlegend=show,
                marker_color=color,
                hovertemplate=(
                    f"<b>{label}</b><br>{x_lbl}<br>"
                    "J(P,Q): %{y:.4f}<extra></extra>"
                ),
            ),
            row=1, col=1,
        )

    # Right panel: KL(P||Q) and KL(Q||P) for each pair, each bucket
    # Use lighter/darker shade to distinguish direction within a pair
    labels_seen_kl: set = set()
    for _, row in div_df.iterrows():
        label = row["label"]
        color = PAIR_COLORS_DIV[label]
        x_lbl = f"Steps {int(row['lo'])}–{int(row['hi'])}"

        # KL(P||Q) — full opacity
        show_pq = f"{label}|PQ" not in labels_seen_kl
        labels_seen_kl.add(f"{label}|PQ")
        fig.add_trace(
            go.Bar(
                x=[x_lbl],
                y=[row["kl_pq"]],
                name=f"{label}  KL(P‖Q)",
                legendgroup=f"{label}_kl",
                showlegend=show_pq,
                marker_color=color,
                marker_opacity=0.90,
                hovertemplate=(
                    f"<b>{label}</b><br>{x_lbl}<br>"
                    "KL(P‖Q): %{y:.4f}<extra></extra>"
                ),
            ),
            row=1, col=2,
        )

        # KL(Q||P) — hatched / semi-transparent
        show_qp = f"{label}|QP" not in labels_seen_kl
        labels_seen_kl.add(f"{label}|QP")
        fig.add_trace(
            go.Bar(
                x=[x_lbl],
                y=[row["kl_qp"]],
                name=f"{label}  KL(Q‖P)",
                legendgroup=f"{label}_kl",
                showlegend=show_qp,
                marker_color=color,
                marker_opacity=0.40,
                marker_line=dict(color=color, width=1.2),
                hovertemplate=(
                    f"<b>{label}</b><br>{x_lbl}<br>"
                    "KL(Q‖P): %{y:.4f}<extra></extra>"
                ),
            ),
            row=1, col=2,
        )

    fig.update_layout(
        title=dict(
            text=(
                "MC-KDE Divergences Between Generation Conditions<br>"
                "<sup>Pairwise distances between latent-distance distributions "
                f"across {n_buckets} denoising-step buckets  |  "
                f"MC samples: {n_samples:,}  |  KDE: Scott's rule</sup>"
            ),
            font=dict(size=15),
            x=0.5,
            xanchor="center",
        ),
        barmode="group",
        bargap=0.18,
        bargroupgap=0.05,
        template="plotly_white",
        font=dict(family="Arial, sans-serif", size=12),
        legend=dict(
            title=dict(text="Condition pair", font=dict(size=11)),
            borderwidth=1,
            bordercolor="#CCCCCC",
            tracegroupgap=4,
        ),
        margin=dict(l=75, r=30, t=130, b=80),
        width=1200,
        height=500,
        plot_bgcolor="white",
        paper_bgcolor="white",
    )

    for col in (1, 2):
        fig.update_xaxes(
            title_text="Denoising step bucket  (early → late)",
            tickangle=-30,
            gridcolor="#E8E8E8",
            row=1, col=col,
        )
    fig.update_yaxes(
        title_text="Jeffrey's divergence  J(P, Q)",
        gridcolor="#E8E8E8", zeroline=True, zerolinecolor="#CCCCCC",
        row=1, col=1,
    )
    fig.update_yaxes(
        title_text="KL divergence  (nats)",
        gridcolor="#E8E8E8", zeroline=True, zerolinecolor="#CCCCCC",
        row=1, col=2,
    )

    # Annotation: note on KL sign (can be negative when KDE support is imperfect)
    fig.add_annotation(
        text=(
            "Note: KL values are MC estimates via KDE and may be slightly negative "
            "due to finite-sample KDE boundary effects.  |  "
            "Jeffrey's divergence is symmetric and always ≥ 0 in expectation."
        ),
        xref="paper", yref="paper",
        x=0.5, y=-0.17,
        showarrow=False,
        font=dict(size=9, color="#888888"),
        xanchor="center",
    )

    return fig


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def parse_args():
    p = argparse.ArgumentParser(
        description="Plot time-resolved distance histograms for the composability gap"
    )
    p.add_argument("--data-dir",   default="experiments/inversion/gap_analysis",
                   help="Directory containing trajectory_distances.json")
    p.add_argument("--output-dir", default="experiments/inversion/gap_analysis",
                   help="Directory to write output figures")
    p.add_argument("--n-buckets",  type=int, default=8,
                   help="Number of timestep buckets (default 8)")
    p.add_argument("--curve",      action="store_true",
                   help="Render as smooth KDE curves instead of bars")
    p.add_argument("--divergence", action="store_true",
                   help="Build MC-KDE divergence bar charts (Jeffrey's + KL)")
    p.add_argument("--mc-samples", type=int, default=20_000,
                   help="Monte Carlo samples for KL estimation (default 20000)")
    return p.parse_args()


def _save(fig: go.Figure, stem: str, out_dir: Path) -> None:
    html_path = out_dir / f"{stem}.html"
    fig.write_html(str(html_path))
    print(f"  Saved {html_path}")

    png_path = out_dir / f"{stem}.png"
    try:
        fig.write_image(str(png_path), scale=2)
        print(f"  Saved {png_path}")
    except Exception as e:
        print(f"  PNG skipped ({type(e).__name__}: {e})")


def main():
    args    = parse_args()
    data_dir = Path(args.data_dir)
    out_dir  = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"Loading trajectory_distances.json from {data_dir} ...")
    records = load_records(data_dir)

    df = pd.DataFrame(records)
    print(f"  {len(records):,} records — "
          f"{df['pair'].nunique()} pairs × "
          f"{df['seed'].nunique()} seeds × "
          f"{df['step'].nunique()} steps")

    if args.divergence:
        print(f"\nComputing MC-KDE divergences ({args.n_buckets} buckets, "
              f"{args.mc_samples:,} MC samples) ...")
        fig  = build_divergence_figure(records, n_buckets=args.n_buckets,
                                       n_samples=args.mc_samples)
        stem = "trajectory_divergence"
        _save(fig, stem, out_dir)
    else:
        mode = "curve" if args.curve else "bars"
        print(f"\nBuilding time-resolved histogram ({args.n_buckets} buckets, mode={mode}) ...")
        if args.curve:
            fig  = build_figure_curve(records, n_buckets=args.n_buckets)
            stem = "trajectory_histogram_curve"
        else:
            fig  = build_figure(records, n_buckets=args.n_buckets)
            stem = "trajectory_histogram"
        _save(fig, stem, out_dir)

    print("\nDone.")


